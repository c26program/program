#include<iostream>
using namespace std;
int main()
{
	int a=3;
	int b=a++; 
	cout<<a<<"\n";
	cout<<b<<"\n";
	int x=6;
	int y=++x;
	cout<<y<<"\n";
	cout<<x<<"\n";
	int c=7;
	int d=c--;
	cout<<d<<"\n";
	cout<<c<<"\n";
	int r=9;
	int s=--r;
	cout<<s<<"\n";
	cout<<r<<"\n";
	
	
	
}
